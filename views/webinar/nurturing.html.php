<p>I work in sales for a living. The whole art form has been flipped upside down and turned inside out. The sales profession will never be the same again.</p>

<p>I know what you’re thinking, “But I don’t work in sales.” Yes, you do. We all do. Every damn day you’re selling whether you have ‘sales’ in your job title or not.</p>

<p>The skill of selling is crucial. And trying to sell when you can’t talk to people face to face is much harder and requires a whole new approach.</p>
<h3>The New Approach to Sales</h3>

<p>Right now, none of my clients are buying. They especially don’t want to be sold to. My sales pipelines have gone from piping hot to ice cold, overnight.</p>

<p>This means if you send emails and make phone calls with the intent of "selling" something, you could be left out in the cold.</p>

<p>A reframe is needed in this new environment. You have two new objectives:
<ul>
    <li>Nurture your existing relationships</li>
    <li>Increase the number of new relationships</li>
</ul>
</p>
<p>These are the two tasks you complete daily when selling during a pandemic/recession. It’s not about the number of sales or how much revenue you generate but the number of touchpoints and meaningful conversations you have.</p>

<p>Why aren’t people buying at the moment?
<ul>
    <li>Their families are dealing with a virus and perhaps the loss of loved ones</li>
    <li>The news channel has 24/7 round the clock horrifying images and headlines</li>
    <li>Company’s business models are changing</li>
    <li>Entire industries have closed down or been placed on hold</li>
    <li>Unemployment has skyrocketed</li>
    <li>Redundancies are a daily occurrence</li>
				</ul>
</p>
<p>
How can any prospect make a logical buying decision with all of this going on? They mostly can’t. A decision on a proposal during this time could be a death wish for their career if they get it wrong.</p>
